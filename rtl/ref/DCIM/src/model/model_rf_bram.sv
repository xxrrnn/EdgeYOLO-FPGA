// ============================================================================
// model_rf_bram - BRAM 推断友好的 RAM 模型
// ============================================================================
// Vivado 会自动将此模块推断为 Block RAM
// 特点：
//   - 同步读写
//   - 全字写入（无位掩码，简化 BRAM 使用）
//   - 读优先模式
// ============================================================================

module model_rf_bram #(
    parameter int WIDTH = 128,
    parameter int DEPTH = 128
)(
    input  logic                        clk,
    input  logic                        cen,    // 片选（低有效）
    input  logic                        gwen,   // 全局写使能（低有效）
    input  logic [WIDTH-1:0]            wen,    // 位掩码（低有效）- 保留接口但忽略
    input  logic [WIDTH-1:0]            d,      // 写数据
    input  logic [$clog2(DEPTH)-1:0]    a,      // 地址
    output logic [WIDTH-1:0]            q       // 读数据
);

    // ========================================================================
    // RAM 声明 - Vivado BRAM 推断风格
    // ========================================================================
    (* ram_style = "block" *) 
    reg [WIDTH-1:0] mem [0:DEPTH-1];

    // ========================================================================
    // 同步读写逻辑 - 简化版（无位掩码）
    // ========================================================================
    always @(posedge clk) begin
        if (~cen) begin  // 片选有效
            if (~gwen) begin
                // 全字写入（忽略 wen 位掩码）
                mem[a] <= d;
            end
            // 读操作：Read-First 模式
            q <= mem[a];
        end
    end

endmodule
